import React from 'react'
import {
  CAvatar,
  CBadge,
  CDropdown,
  CDropdownDivider,
  CDropdownHeader,
  CDropdownItem,
  CDropdownMenu,
  CDropdownToggle,
} from '@coreui/react'
import {
  cilBell,
  cilCreditCard,
  cilCommentSquare,
  cilEnvelopeOpen,
  cilFile,
  cilLockLocked,
  cilSettings,
  cilTask,
  cilUser,
} from '@coreui/icons'
import CIcon from '@coreui/icons-react'
import { useEffect, useMemo } from 'react'
import { useDispatch, useSelector } from 'react-redux'
import avatar8 from './../../assets/images/avatars/8.jpg'
import { getProfile } from '../../redux/slice/companySlice'
const AppHeaderDropdown = () => {
  const dispatch = useDispatch()

  // Redux store se profile aur loading state nikalna
  const { profile } = useSelector((state) => state.companies)
  const BASE_URL = 'https://localhost:7016'

  // Professional Way: Header load hotay hi profile data fetch karega
  useEffect(() => {
    const user = JSON.parse(localStorage.getItem('user'))
    // Agar store mein profile nahi hai, tabhi fetch karein (Performance optimize)
    if (!profile && (user?.userId || user?.id)) {
      dispatch(getProfile(user.userId || user.id))
    }
  }, [dispatch, profile])

  // Avatar Image Path Logic
  const avatarImage = useMemo(() => {
    if (profile?.profilePictureUrl) {
      const path = profile.profilePictureUrl.startsWith('/')
        ? profile.profilePictureUrl
        : `/${profile.profilePictureUrl}`
      return `${BASE_URL}${path}`
    }
    return 'https://via.placeholder.com/150' // Default if no image
  }, [profile?.profilePictureUrl])
  return (
    <CDropdown variant="nav-item">
      <CDropdownToggle placement="bottom-end" className="py-0 pe-0" caret={false}>
        <CAvatar src={avatarImage} size="md" />
      </CDropdownToggle>
      <CDropdownMenu className="pt-0" placement="bottom-end">
        <CDropdownHeader className="bg-body-secondary fw-semibold mb-2">Account</CDropdownHeader>
        <CDropdownItem href="#">
          <CIcon icon={cilBell} className="me-2" />
          Updates
          <CBadge color="info" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilEnvelopeOpen} className="me-2" />
          Messages
          <CBadge color="success" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilTask} className="me-2" />
          Tasks
          <CBadge color="danger" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilCommentSquare} className="me-2" />
          Comments
          <CBadge color="warning" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownHeader className="bg-body-secondary fw-semibold my-2">Settings</CDropdownHeader>
        <CDropdownItem href="#">
          <CIcon icon={cilUser} className="me-2" />
          Profile
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilSettings} className="me-2" />
          Settings
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilCreditCard} className="me-2" />
          Payments
          <CBadge color="secondary" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownItem href="#">
          <CIcon icon={cilFile} className="me-2" />
          Projects
          <CBadge color="primary" className="ms-2">
            42
          </CBadge>
        </CDropdownItem>
        <CDropdownDivider />
        <CDropdownItem href="#">
          <CIcon icon={cilLockLocked} className="me-2" />
          Lock Account
        </CDropdownItem>
      </CDropdownMenu>
    </CDropdown>
  )
}

export default AppHeaderDropdown
